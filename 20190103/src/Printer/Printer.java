package Printer;

public  class Printer {

    public  void println(int number){
        System.out.println(number);
    }
    public  void println(String name){
        System.out.println(name);
    }
    public  void println(boolean tf){
        System.out.println(tf);
    }
    public  void println(double dnumber){
        System.out.println(dnumber);
    }
    //static 사용
    public static void println2(int number){
        System.out.println(number);
    }
    public static void println2(String name){
        System.out.println(name);
    }
    public static void println2(boolean tf){
        System.out.println(tf);
    }
    public static void println2(double dnumber){
        System.out.println(dnumber);
    }


}
